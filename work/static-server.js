function sendJson(res, status, data) {
  res.writeHead(status, { "content-type": "application/json; charset=utf-8" });
  res.end(JSON.stringify(data));
}

function readJson(req) {
  return new Promise((resolve, reject) => {
    let body = "";
    req.on("data", (chunk) => {
      body += chunk;
      if (body.length > 8 * 1024 * 1024) {
        req.destroy();
        reject(new Error("request body too large"));
      }
    });
    req.on("end", () => {
      try {
        resolve(body ? JSON.parse(body) : {});
      } catch (error) {
        reject(new Error(`请求 JSON 格式错误：${error.message}`));
      }
    });
    req.on("error", reject);
  });
}

function todayKey() {
  return new Date().toISOString().slice(0, 10);
}

function clientIp(req) {
  return String(req.headers["x-forwarded-for"] || req.socket.remoteAddress || "")
    .split(",")[0]
    .trim();
}

function checkInviteAndLimit(req, pathname, body) {
  const code = String(body.inviteCode || "").trim();
  if (!code) {
    const error = new Error("请输入邀请码。");
    error.status = 401;
    throw error;
  }
  if (!inviteCodes.has(code)) {
    const error = new Error("邀请码无效，请检查后重试。");
    error.status = 403;
    throw error;
  }
  const limit = endpointLimits[pathname] ?? 3;
  const key = `${todayKey()}:${code}:${pathname}`;
  const current = usageStore.get(key) || 0;
  if (current >= limit) {
    const error = new Error(`今日次数已用完：${pathname} 每个邀请码每天最多 ${limit} 次。`);
    error.status = 429;
    throw error;
  }
  usageStore.set(key, current + 1);
  return {
    inviteCode: code,
    ip: clientIp(req),
    used: current + 1,
    limit
  };
}

function extractJson(text) {
  const cleaned = String(text || "").trim();
  const fenced = cleaned.match(/```(?:json)?\s*([\s\S]*?)```/i);
  const raw = fenced ? fenced[1].trim() : cleaned;
  try {
    return JSON.parse(raw);
  } catch (_) {
    const start = raw.indexOf("{");
    const end = raw.lastIndexOf("}");
    if (start >= 0 && end > start) return JSON.parse(raw.slice(start, end + 1));
    throw new Error("model did not return valid JSON");
  }
}

async function callZhipu(messages, options = {}) {
  if (!zhipuApiKey) {
    const error = new Error("missing ZHIPU_API_KEY");
    error.code = "NO_API_KEY";
    throw error;
  }
  const response = await fetch(`${zhipuBaseUrl.replace(/\/$/, "")}/chat/completions`, {
    method: "POST",
    headers: {
      "content-type": "application/json",
      authorization: `Bearer ${zhipuApiKey}`
    },
    body: JSON.stringify({
      model: options.model || zhipuModel,
      messages,
      temperature: options.temperature ?? 0.75,
      top_p: options.top_p ?? 0.9,
      max_tokens: options.max_tokens ?? 2600
    })
  });
  const body = await response.text();
  if (!response.ok) {
    const error = new Error(`Zhipu API ${response.status}: ${body.slice(0, 500)}`);
    error.status = response.status;
    throw error;
  }
  const data = JSON.parse(body);
  return data.choices?.[0]?.message?.content || "";
}

